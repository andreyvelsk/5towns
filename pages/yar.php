        <h2>Достопримечательности</h2>
        <p>
        Что посмотреть в Ярославле в первую очередь? Облик города формируют церкви в историческом центре — на правом берегу реки Которосли; сохранившиеся старинные здания; знаковые памятники — основателю городу Ярославу Мудрому и медведю, поэту и писателю Н. А. Некрасову, скульптурная композиция «Троица».</p>

        <p>В достопримечательности Ярославля входят ухоженные парки и скверы, музеи с весьма интересными краеведческими и художественными коллекциями, а также Театр Волкова — первый театр, который открыт именно для публичного доступа.</p>

        <P>Само собой, не получится пройти мимо Волжской набережной города, просто поражающей своим великолепием. Кроме того, от вашего взгляда не спрячется знаменитая Стрелка — мыс при впадении в Волгу Которосли. Предание гласит, что именно Стрелка является местом основания города.</P>

        <P>Это главные достопримечательности Ярославля, но на самом деле на то, чтобы осмотреть их все, вам не хватит одного визита в город — вы, несомненно, захотите возвратиться вновь и увидеть остальные красоты города.
        </p>
