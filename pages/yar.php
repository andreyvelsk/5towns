        <h2>История города</h2>
        <p>Древнейшее поселение на территории города обнаружено на левом берегу Волги напротив Стрелки (мыса при слиянии Волги и Которосли) и относится к V—III тысячелетию до н. э. (неолит). В IX веке под Ярославлем сформировалось крупное скандинавско-славянское поселение, известное по комплексу могильных курганов в Тимерёве. При раскопках были обнаружены скандинавское оружие, рунические надписи, шахматные фигурки и крупнейшие на севере Европы клады арабских монет (древнейшие были отчеканены первым из Идрисидов). Из Тимерёва происходит четвёртая часть найденных на Руси скандинавских фибул. По всей видимости, этот «прото-Ярославль» служил крупным центром на Волжском торговом пути. Его отношение к позднейшему Ярославлю (в то время мерянскому поселению Медвежий Угол) можно сравнить с соотношением Гнёздова и Смоленска, Рюрикова городища и Новгорода, Сарского городища и Ростова. Вскоре после основания Ярославля это поселение пришло в упадок, вероятно, в связи с прекращением функционирования Волжского торгового пути. Выше по течению Волги, сразу за границами современного города, археологами изучен крупный Михайловский некрополь с преобладанием рядовых захоронений финно-угорского типа.</p>
    </div>

