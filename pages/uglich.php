        <h2>Достопримечательности</h2>
<p>Углич является одним из городов Золотого кольца России, часто посещается туристами (в городе останавливаются круизные теплоходы, следующие по Волге). В городе сохранились многочисленные образцы традиционной русской архитектуры</p>

<p>Среди основных достопримечательностей Углича:</p>

    <ul>
        <li>
            Угличский кремль с церковью Димитрия на Крови (1692)
        </li>
        <li>
            Свято-Воскресенский монастырь.
        </li>
        <li>
            Богоявленский монастырь с грузным собором (1843—1853).
        </li>
        <li>
            Алексеевский монастырь (1371) с Успенской («Дивной») церковью (1628).
        </li>
        <li>
            Церковь Рождества Иоанна Предтечи (1689—1690).
        </li>
        <li>
            Ансамбль Торговой площади — Торговые ряды (1860), купеческие дома.
        </li>
        <li>
            Старейшее деревянное жилое здание первой трети XVIII века (Дом Меховых-Ворониных)
        </li>
        <li>
            Гражданская застройка города XVIII—XIX веков (Супоневский дворец, Зимин двор) (В руинном состоянии).
        </li>
    </ul>
<p>В 11 км от города, в селе Улейма, расположен Николо-Улейминский монастырь с ансамблем допетровской архитектуры. Другой древний пригородный монастырь, Покровский Паисиев, был разрушен в советское время в результате строительства ГЭС.</p>
