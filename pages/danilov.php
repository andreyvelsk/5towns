        <h2>Достопримечательности</h2>
        <p>Среди основных архитектурных достопримечательностей города: Казанский монастырь на Горушке (1918 г.), дом купца Ворохобина (конец XIX в.), комплекс пожарного депо (1864—1872 гг.), здание городского училища (сер. XIX в.), дом купца Кругликова (кон. XIX в.), здание полицейского управления (конец XIX в.), здание номеров Ягодиной (кон. XIX в.), здание гостиницы Ягодиной (кон. XIX в.).</p>

        <p>Местом памяти для даниловцев является Аллея Героев Советского Союза, земляков-даниловцев, где в самом центре возвышается фигура русского солдата, у подножия которого зажжён Вечный огонь. В центре города стоит памятник павшим борцам за установление советской власти в городе.</p>
